     private boolean validateInput() {
        if (txtMaPhong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Vui lòng nhập mã phòng!",
                "Thông báo", JOptionPane.WARNING_MESSAGE);
            txtMaPhong.requestFocus();
            return false;
        }
        
        if (txtTenPhong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Vui lòng nhập tên phòng!",
                "Thông báo", JOptionPane.WARNING_MESSAGE);
            txtTenPhong.requestFocus();
            return false;
        }
        
        try {
            Integer.parseInt(txtDienTich.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Diện tích phải là số!",
                "Thông báo", JOptionPane.WARNING_MESSAGE);
            txtDienTich.requestFocus();
            return false;
        }
        
        return true;
    }
    
   private Phong getPhongFromInput() {
    String maphong = txtMaPhong.getText().trim();
    String tenphong = txtTenPhong.getText().trim();
    int dientich = Integer.parseInt(txtDienTich.getText().trim());
    String trangthai = cboTrangThai.getSelectedItem().toString();

    Integer maloaiphong = cbMaLoai.getSelectedItem() == null
            ? null : Integer.parseInt(cbMaLoai.getSelectedItem().toString());

    Integer makhach = cbMaKhach.getSelectedItem() == null
            ? null : Integer.parseInt(cbMaKhach.getSelectedItem().toString());
Integer mats = cbMaTaiSan.getSelectedItem() == null
            ? null : Integer.parseInt(cbMaTaiSan.getSelectedItem().toString());
  

    return new Phong(maphong, tenphong, dientich, trangthai,
                     maloaiphong, makhach,mats );

   }
    @Override

public void hienThiDanhSach(List<Phong> list) {
    DefaultTableModel model = (DefaultTableModel) tblPhong.getModel();
    model.setRowCount(0);

    for (Phong p : list) {
        model.addRow(new Object[]{
            p.getMaphong(),
            p.getTenphong(),
            p.getMaloaiphong(),
            p.getMakhach(),
            p.getMats(),
            p.getDientich(),
            p.getTrangthai()
        });
    }
}

    
